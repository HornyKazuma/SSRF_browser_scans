// Начальный IP-адрес
let ipBase = '10.';

// Проходим через все возможные IP-адреса в сети
for (let i = 0; i <= 255; i++) {
    for (let j = 0; j <= 255; j++) {
        let ip = ipBase + i + '.' + j + '.1';

        // Отправляем запрос на каждый IP-адрес
        fetch(`http://${ip}`)
            .then(response => {
                // Если получен ответ, выводим IP-адрес в консоль
                console.log(`Response from: ${ip}`);
            })
            .catch(error => {
                // Если есть ошибка (например, нет ответа), просто игнорируем ее
            });
    }
}